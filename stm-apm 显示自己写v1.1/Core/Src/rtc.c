HAL_StatusTypeDef RTC_Set_Time(u8 hour,u8 min,u8 sec)
{
	RTC_TimeTypeDef RTC_TimeStructure;
	
	RTC_TimeStructure.Hours=hour;
	RTC_TimeStructure.Minutes=min;
	RTC_TimeStructure.Seconds=sec;
//	RTC_TimeStructure.TimeFormat=ampm;
	RTC_TimeStructure.DayLightSaving=RTC_DAYLIGHTSAVING_NONE;
    RTC_TimeStructure.StoreOperation=RTC_STOREOPERATION_RESET;
	return HAL_RTC_SetTime(&hrtc,&RTC_TimeStructure,RTC_FORMAT_BCD);	
}

//RTC日期设置
//year,month,date:年(0~99),月(1~12),日(0~31)
//week:星期(1~7,0,非法!)
//返回值:SUCEE(1),成功
//       ERROR(0),进入初始化模式失败 
HAL_StatusTypeDef RTC_Set_Date(u8 year,u8 month,u8 date)
{
//	RTC_DateTypeDef RTC_DateStructure;
    
	sdatestructure.Date=date;
	sdatestructure.Month=month;
//	RTC_DateStructure.WeekDay=week;
	sdatestructure.Year=year;
	return HAL_RTC_SetDate(&hrtc,&sdatestructure,RTC_FORMAT_BCD);
}
